#There are a number of variables listed below. What are the different types and how do you know which is which?

excited_dog = "excited dog"
@excited_dog = "excited dog"
@@excited_dog = "excited dog"

=begin
excited_dog is a local variable. You can notice that because no @ or @@ is prefixed.
@excited_dog is an instance variable. The @ indicates that.
@@excited_dog is a class variable. The @@ indicates that.

=end

